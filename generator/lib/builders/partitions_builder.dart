String buildPartitions(Map<String, dynamic> partitionsJson) {
  final version = partitionsJson['version'] as String;
  final partitions = (partitionsJson['partitions'] as List).cast<Map>();

  final buf = StringBuffer()
    ..writeln('// AUTO-GENERATED: This file is generated by the `generator` '
        'package.')
    ..writeln('// Source: aws-sdk-js-v3 '
        'packages-internal/util-endpoints/src/lib/aws/partitions.json')
    ..writeln('//')
    ..writeln('// The AWS partition table used by the `aws.partition` endpoint '
        'rule function.')
    ..writeln()
    ..writeln("import 'endpoint_partition.dart';")
    ..writeln()
    ..writeln("const String partitionsVersion = '$version';")
    ..writeln()
    ..writeln('const List<EndpointPartition> partitions = [');

  for (final partition in partitions) {
    final outputs = (partition['outputs'] as Map).cast<String, Object?>();
    final regions =
        ((partition['regions'] as Map?)?.keys.cast<String>().toList() ?? [])
          ..sort();
    buf
      ..writeln('  EndpointPartition(')
      ..writeln("    id: '${partition['id']}',")
      ..writeln("    regionRegex: r'${partition['regionRegex']}',")
      ..writeln('    regions: {${regions.map((r) => "'$r'").join(', ')}},')
      ..writeln('    outputs: EndpointPartitionOutputs(')
      ..writeln("      name: '${outputs['name']}',")
      ..writeln("      dnsSuffix: '${outputs['dnsSuffix']}',")
      ..writeln("      dualStackDnsSuffix: '${outputs['dualStackDnsSuffix']}',")
      ..writeln('      supportsFips: ${outputs['supportsFIPS']},')
      ..writeln('      supportsDualStack: ${outputs['supportsDualStack']},')
      ..writeln(
          "      implicitGlobalRegion: '${outputs['implicitGlobalRegion']}',")
      ..writeln('    ),')
      ..writeln('  ),');
  }

  buf.writeln('];');
  return buf.toString();
}
