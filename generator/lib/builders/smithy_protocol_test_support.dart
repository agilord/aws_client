import 'dart:convert';

Map<String, String> parseSkipList(String json) {
  if (json.trim().isEmpty) return {};
  final decoded = jsonDecode(json) as Map<String, dynamic>;
  return decoded.map((k, v) => MapEntry(k, v as String));
}

List<String> staleSkipKeys(
        Map<String, String> skipList, Set<String> emittedIds) =>
    skipList.keys.where((k) => !emittedIds.contains(k)).toList();

class ProtocolReadiness {
  final String protocol;
  final int corpusVectors;
  final int vectors;
  final int skipped;
  final int excludedOps;
  final bool buildFailed;
  ProtocolReadiness({
    required this.protocol,
    required this.corpusVectors,
    required this.vectors,
    required this.skipped,
    required this.excludedOps,
    this.buildFailed = false,
  });
  int get passing => vectors - skipped;
}

String renderReadiness(List<ProtocolReadiness> rows) {
  final buf = StringBuffer()
    ..writeln('# Smithy Suite 2 — readiness')
    ..writeln()
    ..writeln('Generated by the build. Honest pass/skip/exclude counts for the '
        'official Smithy protocol conformance vectors. "Corpus" is the total '
        'vectors for the protocol; "Passing" out of "Corpus" is true coverage.')
    ..writeln()
    ..writeln(
        '| Protocol | Corpus | Vectors | Passing | Skipped | Excluded ops |')
    ..writeln('|---|---|---|---|---|---|');
  var c = 0, v = 0, p = 0, s = 0, e = 0;
  for (final r in rows) {
    final excludedCell = r.buildFailed
        ? '${r.excludedOps} (whole client failed to build)'
        : '${r.excludedOps}';
    buf.writeln(
        '| ${r.protocol} | ${r.corpusVectors} | ${r.vectors} | ${r.passing} | ${r.skipped} | $excludedCell |');
    c += r.corpusVectors;
    v += r.vectors;
    p += r.passing;
    s += r.skipped;
    e += r.excludedOps;
  }
  buf.writeln('| **total** | **$c** | **$v** | **$p** | **$s** | **$e** |');
  return '$buf';
}
